

//Operator in Knotlin

fun main() {
    val a = 10
    val b = 5
    var result: Int

    result = a + b
    println("a + b = $result")

    result = a - b
    println("a - b = $result")

    result = a * b
    println("a * b = $result")

    result = a / b
    println("a / b = $result")

    result = a % b
    println("a % b = $result")
}
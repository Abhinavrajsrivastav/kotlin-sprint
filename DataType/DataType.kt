//Data types in Kotlin

fun main(args: Array<String>) {
    //Int
    var number: Int = 10
    println("Integer value is: $number")

    //Long
    var longNumber: Long = 1000000000000000000
    println("Long value is: $longNumber")

    //Float
    var floatNumber: Float = 10.5f
    println("Float value is: $floatNumber")

    //Double
    var doubleNumber: Double = 10.5
    println("Double value is: $doubleNumber")

    //Boolean
    var flag: Boolean = true
    println("Boolean value is: $flag")

    //Char
    var letter: Char = 'A'
    println("Character value is: $letter")

    //String
    var name: String = "Kotlin"
    println("String value is: $name")
}

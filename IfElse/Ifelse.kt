

//If Else 

fun main(args: Array<String>) {
    val number = 10
    if (number > 0) {
        println("Positive Number")
    } else {
        println("Negative Number")
    }

    //If Else If
    val number2 = 0
    if (number2 > 0) {
        println("Positive Number")
    } else if (number2 < 0) {
        println("Negative Number")
    } else {
        println("Zero")
    }

    //If Else If Else
    val number3 = 0
    if (number3 > 0) {
        println("Positive Number")
    } else if (number3 < 0) {
        println("Negative Number")
    } else {
        println("Zero")
    }
}